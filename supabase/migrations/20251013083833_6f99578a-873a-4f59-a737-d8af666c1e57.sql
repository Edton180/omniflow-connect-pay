-- Corrigir search_path das funções existentes
CREATE OR REPLACE FUNCTION update_catalog_category_updated_at()
RETURNS TRIGGER 
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

